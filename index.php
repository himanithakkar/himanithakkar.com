	                                    <p>It’s so weird to think that ice has no future!
If you see a perfectly melted ice cube you could never know if it were an ice cube before because all you can see is just water. All the traces of it being an ice cube just vanishes. Is this how we will look like? No matter what we do and where we are we all will just one day vanish with nothing to leave behind? I mean nothing that was not already there just like ice, it was always H2O and it’d still be H2O when it melts but we can never know how the ice looked like when it was there or worse can’t even prove if it existed in the first place. We all wanna believe in something bigger than us like we have to live up to something because it will be remembered and it will stay forever, but what happens when last of the people who knew you are gone? and How could you even be bothered by how people will remember you as when you are gone? There is no way you could ever know! </p>
